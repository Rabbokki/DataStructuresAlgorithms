public class SwitchTest {
    public static void main(String[] args) {

        int n = 1;

        switch (n) {
            case 1:
                System.out.println("n = " + n);
                break;
            case 2:
                System.out.println("n = " + n);
                break;
            case 3:
                System.out.println("n = " + n);
                break;

            default:
                System.out.println("범위 벗어남");
        }
    }
}
